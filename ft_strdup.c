#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		len;
	char	*res;
	int		i;

	i = 0;
	len = ft_strlen(str);
	res = malloc(sizeof(char) * len + 1);
	if (!res)
		return (NULL);
	while (str[i])
	{
		res[i] = str[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
